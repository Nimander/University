#include <iostream>
#include <cmath>
#include <cstdlib>

using namespace std;

int main(){
	double A[6][6] = { { 30.0,2.0/3.0,0.0,0.0,0.0,0.0 },
						{ 3.0/4.0,20.0,5.0/6.0,0.0,0.0,0.0 },
						{ 0.0,7.0/8.0,10.0,9.0/10.0,0.0,0.0 },
						{ 0.0,0.0,11.0/12.0,10.0,13.0/14.0,0.0 },
						{ 0.0,0.0,0.0,15.0/16.0,20.0,17.0/18.0 }, 
						{ 0.0,0.0,0.0,0.0,19.0/20.0,30.0 } };
	double b[6] = {94.0/3.0,173.0/4,581.0/20.0,-815.0/28.0,-6301.0/144.0,-319.0/10.0};
	double x[6];
	
	
	for (int i = 1; i < 6; i++) {
		//WZÓR1
		A[i][i] = A[i][i] - A[i][i - 1] / A[i - 1][i - 1] * A[i-1][i];
		//WZÓR2
		b[i] = b[i]- A[i][i - 1] / A[i - 1][i - 1] * b[i-1];
		A[i][i-1] = 0;
	}
		
	//obliczenie rozwi¹zania	
	x[5] = b[5] / A[5][5];									 
	for (int j = 4; j >= 0; j--)	
		x[j] = (b[j]-A[j][j+1]*x[j+1]) / A[j][j];

	for(int i = 0; i < 6; i++)
		cout << x[i] << ' ';
	
	
	
	
}




